namespace CryEngine
{
	/// <summary>
	/// Primitive shapes that can be loaded from the engine assets.
	/// </summary>
	public enum Primitives
	{
		/// <summary>
		/// A 1x1x0.25 box.
		/// </summary>
		Box,
		/// <summary>
		/// A 1x1x1 cube.
		/// </summary>
		Cube,
		/// <summary>
		/// A cylinder with a diameter of 1 unit and a height of 1 unit.
		/// </summary>
		Cylinder,
		/// <summary>
		/// A 1x1 plane.
		/// </summary>
		Plane,
		/// <summary>
		/// A pyramid shape of 1x1x0.75.
		/// </summary>
		Pyramid,
		/// <summary>
		/// A sphere with a diameter of 1 unit.
		/// </summary>
		Sphere
	}
}
